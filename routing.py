def _all_category_options() -> List[Tuple[str, Optional[str]]]:
    seen: set = set()
    out: List[Tuple[str, Optional[str]]] = []
    for category, subcategory, _ in ROUTES:
        key = (category, subcategory)
        if key not in seen:
            seen.add(key)
            out.append((category, subcategory))
    return out


def _route_via_ai(topic: str, main_text: str) -> Optional[RouteResult]:
    """Распределение через GigaChat (Сбер). Работает в РФ, не open-source. При ошибке возвращает None."""
    creds = os.environ.get("GIGACHAT_CREDENTIALS", "").strip()
    if not creds:
        return None
    try:
        from gigachat import GigaChat
    except ImportError:
        return None

    options = _all_category_options()
    options_str = "\n".join(
        f"- {cat} | {subcat if subcat else '(без подкатегории)'}" for cat, subcat in options
    )
    prompt = f"""Ты классификатор обращений граждан. По тексту обращения выбери ОДНУ подходящую категорию из списка ниже.
Ответь СТРОГО в одну строку в формате: КАТЕГОРИЯ|ПОДКАТЕГОРИЯ
Если подкатегории нет в списке для выбранной категории — напиши только КАТЕГОРИЯ|
Текст обращения:
Тема: {topic[:500]}
Описание: {main_text[:2000]}

Список категорий (категория | подкатегория):
{options_str}

Дополнительно можно выбрать: Не определено| (если ни одна категория не подходит).
Ответ (только одна строка формата КАТЕГОРИЯ|ПОДКАТЕГОРИЯ или КАТЕГОРИЯ|):"""

    verify_ssl = os.environ.get("GIGACHAT_VERIFY_SSL_CERTS", "false").lower() == "true"
    try:
        with GigaChat(credentials=creds, verify_ssl_certs=verify_ssl) as client:
            resp = client.chat(prompt)
            raw = (resp.choices[0].message.content or "").strip()
    except Exception:
        return None

    if not raw:
        return None
    parts = raw.split("|", 1)
    chosen_cat = parts[0].strip() if parts else ""
    chosen_sub = (parts[1].strip() or None) if len(parts) > 1 else None
    if not chosen_cat:
        return None
    if chosen_cat == "Не определено" and not chosen_sub:
        return RouteResult("Не определено", None, 0.0, "ai:no_match")

    def _norm(s: str) -> str:
        if not s:
            return ""
        s = s.strip().lower().replace("\u2013", "-").replace("\u2014", "-")
        return re.sub(r"\s+", " ", re.sub(r"\s*-\s*", " - ", s))

    chosen_cat_n = _norm(chosen_cat)
    chosen_sub_n = _norm(chosen_sub) if chosen_sub else ""

    for cat, subcat in options:
        if _norm(cat) != chosen_cat_n:
            continue
        if chosen_sub_n:
            if subcat and _norm(subcat) == chosen_sub_n:
                return RouteResult(cat, subcat, 0.85, f"ai:{raw[:100]}")
        else:
            return RouteResult(cat, subcat, 0.85, f"ai:{raw[:100]}")
    return RouteResult(chosen_cat, chosen_sub or None, 0.7, f"ai:{raw[:100]}")


def _route_via_regex(text: str) -> RouteResult:
    """Исходное распределение по правилам (regex). Все категории из ROUTES сохранены."""
    best: RouteResult = DEFAULT
    best_score = 0.0
    best_hits = []

    for category, subcategory, patterns in ROUTES:
        score = 0.0
        hits = []
        for pat, w in patterns:
            if re.search(pat, text):
                score += w
                hits.append(pat)
        if score > best_score:
            best_score = score
            best = RouteResult(category, subcategory, 0.0, "")
            best_hits = hits

    if best_score == 0.0:
        return DEFAULT
    confidence = min(1.0, best_score / 6.0)
    best.confidence = confidence
    best.debug = f"score={best_score}; hits={best_hits}"
    return best


def route_appeal(topic: str, main_text: str) -> RouteResult:
    text = normalize(f"{topic} {main_text}")
    ai_result = _route_via_ai(topic, main_text)
    if ai_result is not None:
        return ai_result
    return _route_via_regex(text)


def _route_tag_via_ai(topic: str, main_text: str) -> Optional[str]:
    """Выбор тега приоритета через GigaChat. При ошибке возвращает None."""
    creds = os.environ.get("GIGACHAT_CREDENTIALS", "").strip()
    if not creds:
        return None
    try:
        from gigachat import GigaChat
    except ImportError:
        return None

    tags_str = ", ".join(PRIORITY_TAGS)
    prompt = f"""Ты классификатор обращений граждан. По тексту обращения выбери ОДИН тег из списка ниже.
Теги: {tags_str}

Текст обращения:
Тема: {topic[:500]}
Описание: {main_text[:2000]}

Ответь СТРОКОЙ — только одно слово из списка тегов, без точки и кавычек."""

    verify_ssl = os.environ.get("GIGACHAT_VERIFY_SSL_CERTS", "false").lower() == "true"
    try:
        with GigaChat(credentials=creds, verify_ssl_certs=verify_ssl) as client:
            resp = client.chat(prompt)
            raw = (resp.choices[0].message.content or "").strip().strip(".""\"'")
    except Exception:
        return None

    if not raw:
        return None
    raw_n = raw.strip().lower()
    for tag in PRIORITY_TAGS:
        if tag.lower() == raw_n:
            return tag
    return None


def route_tag(topic: str, main_text: str) -> str:
    """Возвращает тег приоритета (ИИ или «Единичный случай» по умолчанию)."""
    tag = _route_tag_via_ai(topic, main_text)
    return tag if tag else "Единичный случай"